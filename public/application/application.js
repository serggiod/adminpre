angular
	.module('adminpre',['ui.materialize','ngRoute','froala'])
	.config(function($routeProvider){
	    $routeProvider
		    .when('/',{
		        templateUrl:'views/login.html',
		        controller:'login'
		    })
		    .when('/login',{
		        templateUrl:'views/login.html',
		        controller:'login'
		    })
		    .when('/logout',{
		        templateUrl:'views/logout.html',
		        controller:'logout'
		    })
		    .when('/password',{
		        templateUrl:'views/password.html',
		        controller:'password'
		    })
		    .when('/acercade',{
		        templateUrl:'views/acercade.html',
		        controller:'acercade'
		    })
		    .when('/manual',{
		        templateUrl:'views/manual.html',
		        controller:'manual'
		    })
		    .when('/partes',{
		        templateUrl:'views/partes.html',
		        controller:'partes'
		    })
		    .when('/partes/nuevo',{
		        templateUrl:'views/partes.nuevo.html',
		        controller:'partesNuevo'
		    })
		    .when('/partes/visualizar/:id',{
		        templateUrl:'views/partes.visualizar.html',
		        controller:'partesVisualizar'
		    })
		    .when('/partes/modificar/:id',{
		        templateUrl:'views/partes.modificar.html',
		        controller:'partesModificar'
		    })
		    .otherwise({redirectTo:'/'});
	});