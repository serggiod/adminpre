angular
	.module('adminpre',['ngRoute'])
	.config(function($routeProvider){
	    $routeProvider
		    .when('/',{
		        templateUrl:'views/login.html',
		        controller:'login'
		    })
		    .when('/login',{
		        templateUrl:'views/login.html',
		        controller:'login'
		    })
		    .when('/logout',{
		        templateUrl:'views/logout.html',
		        controller:'logout'
		    })
		    .when('/password',{
		        templateUrl:'views/password.html',
		        controller:'password'
		    })
		    .when('/acercade',{
		        templateUrl:'views/acercade.html',
		        controller:'acercade'
		    })
		    .when('/manual',{
		        templateUrl:'views/manual.html',
		        controller:'manual'
		    })
		    .when('/partes',{
		        templateUrl:'views/partes.html',
		        controller:'partes'
		    })
		    /*
		    .when('/diputado',{
		        templateUrl:'views/diputado.html',
		        controller:'diputado'
		    })
			*/
		    .otherwise({redirectTo:'/'});
	});